const calcApprovalStatus = (approvals: { version: string; status: ApprovalStatus }[], currentVersion: number): ApprovalStatus => {
  const versionStr = `${currentVersion}.`;
  const versionApprovals = approvals.filter((a) => a.version.startsWith(versionStr));
  if (versionApprovals.length === 0) return 'draft';
  if (versionApprovals.some((a) => a.status === 'rejected')) return 'rejected';
  if (versionApprovals.every((a) => a.status === 'approved')) return 'approved';
  return 'pending';
};

